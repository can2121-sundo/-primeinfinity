let currentLang = localStorage.getItem('primeinfinity-lang') || 'fr';

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('primeinfinity-lang', lang);
  const t = translations[lang];
  if (!t) return;

  // Update all elements with data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // Re-render dynamic product cards when language changes
  if (typeof renderProductCards === 'function') {
    renderProductCards(lang);
    document.querySelectorAll('.products-grid .animate-on-scroll').forEach(el => {
      el.classList.add('fade-in-up');
    });
  }

  // Update lang flag active state
  document.querySelectorAll('.lang-flag').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });

  // Update html lang attribute
  document.documentElement.lang = lang;
}

function toggleLanguage() {
  const langs = ['fr', 'de', 'en'];
  const idx = langs.indexOf(currentLang);
  const nextLang = langs[(idx + 1) % langs.length];
  applyLanguage(nextLang);
}

function initI18n() {
  // Bind click events to lang flags
  document.querySelectorAll('.lang-flag').forEach(btn => {
    btn.addEventListener('click', () => {
      applyLanguage(btn.getAttribute('data-lang'));
    });
  });

  // Detect language: saved first, then browser, then default
  const savedLang = localStorage.getItem('primeinfinity-lang');
  if (savedLang && translations[savedLang]) {
    applyLanguage(savedLang);
  } else {
    const browserLang = navigator.language || navigator.userLanguage;
    if (browserLang.startsWith('de')) applyLanguage('de');
    else if (browserLang.startsWith('fr')) applyLanguage('fr');
    else applyLanguage('en');
  }
}

// Auto-init when DOM is ready
document.addEventListener('DOMContentLoaded', initI18n);

